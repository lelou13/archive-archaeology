                            <td colspan="2" align="left" valign="top"><h1>Wireless 
                                Project Technologies Summary</h1>
                              </td>
                          </tr>
                          <tr> 
                            <td width="59%" align="left" valign="top"><div align="center"> 
                                <p align="left" class="bold">By Ruben G. Mendoza, 
                                  Ph.D.</p>
                                <p align="left">The deployment of appropriate 
                                  technologies in this instance will encompass 
                                  both on-campus learning labs (replete with applied 
                                  research aspects) and archaeological field investigations 
                                  centered on the excavation and mapping of the 
                                  architectural traces of two early California 
                                  Mission sites.</p>
                                <p align="center"><img src="images/e740PDA.gif" width="99" height="155" /></p>
                                <p align="left" class="images-text">For lab and 
                                  field applications both the Toshiba e740 with 
                                  integrated wireless card and the iPaq Pocket 
                                  PC 3950 with separate wireless card module were 
                                  selected for comparative purposes. </p>
                                <p align="left">Where on-campus lab applications 
                                  of wireless technology are concerned, it should 
                                  be noted that the CSU Monterey Bay campus has 
                                  already transitioned into the domain of going 
                                  fully wireless as per its status as a demonstration 
                                  project site for wireless technologies in teaching 
                                  and learning.</p>
                                <p align="left">In this rarified atmosphere, students 
                                  will undertake wireless data capture, data processing 
                                  and transmission, and the upload of primary 
                                  document files to the Internet via wireless 
                                  PDA hand-held computers equipped with SmartPad 
                                  and Seiko Instruments InkLink Handwriting Systems, 
                                  and C-Pen devices for the scanning of historic 
                                  documents pertaining to museum anthropology 
                                  and archaeological research specific to the 
                                  undertakings in question.</p>
                                <p align="left">Where remote field settings necessitate 
                                  a differing approach, the deployment of a virtual 
                                  learning lab consisting of wireless (802.11b 
                                  compatible) PDAs, and a laptop-based field server 
                                  replete with access point and a two-way broadband 
                                  satellite connection will provide the basis 
                                  for wireless transmissions of data, and the 
                                  posting of weekly Online Journal entries and 
                                  &#8220;Wireless Field Reports&#8221; to the 
                                  campus server of the Institute for Archaeological 
                                  Science, Technology, and Visualization.</p>
                                <p align="center"><img src="images/iPaq3955.gif" width="95" height="147" /></p>
                                <p align="left" class="images-text">Despite the 
                                  fact that the iPaq 3950 Pocket PC does not come 
                                  equipped with the integrated wireless option, 
                                  the availability of a wireless card docking 
                                  station was seen as one other prospect for expanding 
                                  the possibilities for peripherals needed for 
                                  the project.</p>
                                <p align="left">The deployment of the virtual 
                                  learning lab replete with remote base stations 
                                  will ensure that project participants are afforded 
                                  every opportunity to post field notes and reports, 
                                  electronic maps and illustrations, Online Journal 
                                  entries, and raw data directly from the field 
                                  setting of an ongoing archaeological investigation.</p>
                                <p align="left"><img src="images/CompaqEvoN610c.gif" width="153" height="142" /><br />
                                  <br />
                                  <span class="images-text">Two Compaq Evo N610c 
                                  Notebook computers will be outfitted with Wireless 
                                  Access Points and deployed in both lab and field 
                                  contexts for the purposes of Internet-based 
                                  real-time data management.</span></p>
                                <p align="left">Because this latter effort will 
                                  be coordinated with the results of a GPR (Ground 
                                  Penetrating Radar) survey, students will have 
                                  the opportunity to communicate their respective 
                                  experiences with both wireless and broadband, 
                                  and remote sensing technology applications in 
                                  archaeology and the social sciences more broadly.</p>
                              </div></td>
                            <td width="41%" align="left" valign="top"><p align="left" class="bold">The 
                                Equipment List</p>
                              <p align="left">The primary devices that will serve 
                                to facilitate the mandated wireless and broadband 
                                connection include the Customized Laptop/Server 
                                to be used in concert with an access point for 
                                connection to the broadband satellite receiver 
                                / transmitter required of this project operation, 
                                as well as a second Customized Laptop that will 
                                serve as a field station for student use and the 
                                manipulation of PDA files and related resources. 
                              </p>
                              <p align="left">The specific connection to the broadband 
                                network will be facilitated by way of the installation 
                                and subscription to a two-way wireless broadband 
                                network facilitated by way of a satellite receiver/transmitter 
                                installed at both the Carmel Mission and Old Mission 
                                San Juan Bautista. Whereas in the instance of 
                                Carmel Mission, the transmitter will be situated 
                                in the Mission's bell tower, at San Juan Bautista 
                                the transmitter will be installed in an unobtrusive 
                                area of the existing convent wing and Old Mission 
                                Museum.</p>
                              <p align="center"><font color="#000000"><img src="images/DirecWaySatellite.gif" width="126" height="146"></font></p>
                              <p align="left" class="images-text">For the purposes 
                                of generating wireless and broadband connectiveity 
                                for the field-based portions of that archaeology 
                                conducted at the Carmel Mission site, a DirecWay 
                                satellite dish like that pictured will be installed 
                                in the bell tower at the Carmel Mission</p>
                              <p align="left" class="images-text"><img src="images/LinksysAccessPoint802.11B.gif" width="185" height="160"></p>
                              <p class="images-text">This Linksys Wireless Access 
                                Point is akin to those that will be used to link 
                                both laptop base stations to the satellite transmitter 
                                situated in the bell tower of Carmel Mission. 
                              </p>
                              <p class="bold">Project Peripherals</p>
                              <p>The primary devices that will serve to facilitate 
                                the mandated wireless and broadband connection 
                                include the Laptop Base Station and Server to 
                                be used in concert with a wireless access point 
                                for connection to the broadband satellite receiver 
                                / transmitter. A second customized laptop will 
                                serve as a Virtual Learning Lab field station 
                                for student use in the wireless upload and manipulation 
                                of PDA files and related data management requirements 
                                during project excavations and lab analysis.</p>
                              <p><img src="images/PharosPocketGPS.gif" width="155" height="138"></p>
                              <p class="images-text">The Pharos GPS Portable Navigator 
                                module will be used in concert with other peripherals 
                                to facilitate a GPS navigated tour of the historic 
                                sector of the 200 year old community of San Juan 
                                Bautista.</p>
                              <p>Other key equipment includes 10 wireless PDA 
                                units for the purposes of provisioning a cohort 
                                of 10 students identified with the wireless test 
                                cohort. All ten units will be upgraded with a 
                                256MB secure digital card for the purposes of 
                                enhancing Internet reception and downloads. Two 
                                of the units will be outfitted with GPS portable 
                                navigators for the purposes of facilitating a 
                                student lab module in satellite-mediated mapping 
                                techniques in archaeology. Two PDAs, and or laptops, 
                                will be outfitted with barcode wand readers for 
                                the purposes of field specimens inventory identification 
                                conducted in concert with the application of self-adhesive 
                                barcode labels applied to any and all specimens 
                                recovered from the field sites specific to archaeology.</p>
                              </td>
