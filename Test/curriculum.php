                            <td colspan="2" align="left" valign="top"><h1>Wireless 
                                Project Curriculum</h1>
                              </td>
                          </tr>
                          <tr> 
                            <td width="59%" align="left" valign="top"><div align="center"> 
                                <p align="left">January 22, 2003 </p>
                                <p align="left">The CSU Monterey Bay Institute 
                                  for Archaeology maintains an active field program 
                                  on the California Central Coast. At present, 
                                  the focus of existing projects centers on the 
                                  archaeology and ethnohistory of the early California 
                                  Missions of San Juan Bautista and San Carlos 
                                  Borromeo del Rio Carmelo or Carmel Mission. 
                                </p>
                                <p align="left">Whereas the project at San Juan 
                                  Bautista has afforded hundreds of CSUMB and 
                                  other California college and university students 
                                  invaluable research opportunities since the 
                                  Fall of 1995, the Carmel Mission Project was 
                                  initiated with the collaboration of the Diocese 
                                  of Monterey's curator, Sir Richard Joseph Menn, 
                                  and Carmel Mission Pastor John Griffin in the 
                                  Spring of 2003. This latter effort was initiated 
                                  so as to determine the location of the lost 
                                  wine cellar at said mission complex.</p>
                                <p align="left" class="bold">Lab and Field Offerings 
                                </p>
                                <p align="left">The Wireless Project will employ 
                                  both the SBS 224s/324s (Archaeology: From Map 
                                  to Museum) and SBS 260s/360s (Archaeology of 
                                  a California Mission) courses as the respective 
                                  contexts for the demonstration project in question. 
                                  In each instance, students matriculated in said 
                                  courses will be taught to make use of the wireless 
                                  technologies made available as the result of 
                                  the grant obtained by the ASTV.</p>
                                <p align="left" class="bold">SBSC 224s/324s -- 
                                  Archaeology: From Map to Museum</p>
                                <p align="left">This course is an introduction 
                                  to the methods, principles, and practices of 
                                  field archaeology. It will combine in-class 
                                  lab and discussion sessions with field studies 
                                  in historical archaeology. Semester lab projects 
                                  include (a) modern material cultures study or 
                                  garbology lab, and (b) flintknapping or stone 
                                  tool production lab.<br />
                                </p>
                                <p align="left"><a href="http://archaeology.csumb.edu/Courses/Archaeology/introduction.htm">SBSC 
                                  224s/324s Online Syllabus</a></p>
                                <p align="left"><img src="images/FlintknappersCutout.jpg" width="162" height="117" /><br />
                                  <br />
                                  <span class="images-text">One of two semester 
                                  learning labs entails a hands-on introduction 
                                  to flintknapping or stone tool production and 
                                  analysis. Here students are pictured working 
                                  obsidian in the production of stone tools.</span></p>
                                <br />
                              </div></td>
                            <td width="41%" align="left" valign="top"><p><img src="images/fieldnotebook.jpg" width="162" height="125" /><br />
                                <br />
                                <span class="images-text">This field notebook 
                                provides an example of the various bits of information 
                                recorded for individual specimens in both lab 
                                and field contexts.</span></p>
                              <p class="bold">SBSC 260s/360s -- Archaeology of 
                                a California Mission</p>
                              <p>This field course engages the archaeological 
                                exploration of methods and practices specific 
                                to the assessment of archaeological and ethnohistorical 
                                data and its interpretation. Student projects 
                                center on the excavation of contact period and 
                                colonial archaeological sites, and the study and 
                                documentation of the early California mission 
                                sites of San Juan Bautista or Carmel Mission. 
                                The Spring 2003 offering will focus on the recovery 
                                of the lost wine cellar of San Carlos Borromeo 
                                del Rio Carmelo. </p>
                              <p><a href="http://archaeology.csumb.edu/Courses/sbsc360sSp2002/index.htm">SBSC 
                                260s/360s Online Syllabus</a></p>
                              <p align="center"><img src="images/vertebrate.jpg" width="162" height="192" class="images-border" /></p>
                              <p align="left" class="images-text">This faunal 
                                vertebrate was recovered during the course of 
                                excavations in the Lost Convento area of Old Mission 
                                San Juan Bautista. It constitutes but one of the 
                                over 5,400 specimens bags filled with animal bone 
                                that were examined and identified in the Summer 
                                of 2001.</p>
